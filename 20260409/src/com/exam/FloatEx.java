package com.exam;

/*
 *	실수형 (float: 4byte 크기의 실수형 타입)
 *	float 선언시 초기값 뒤에 f를 붙여서 float형 임을 명시한다. 
 */

public class FloatEx {

	public static void main(String[] args) {
		
		float var1, var2;
		double var3;
		
		var1 = 3.4f;
		var2 = 55.5f;
		var3 = 12.18;
		
		System.out.println("var1의 값은 " + var1);
		System.out.println("var2의 값은 " + var2);
		System.out.println("var3의 값은 " + var3);
	}

}
